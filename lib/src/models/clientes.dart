class Clientes {
  final String nome;
  final String senha;
  final List<String> servicos;
  final String horario;
  final String avatar;
  final String id;
  final String email;
  final String username;

  const Clientes({
    required this.nome,
    required this.senha,
    required this.servicos,
    required this.horario,
    required this.avatar,
    required this.id,
    required this.email,
    required this.username
  });
}